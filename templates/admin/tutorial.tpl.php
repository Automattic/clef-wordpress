        <h1>Using Clef — 3 important things</h1>
        <h3>1. Sync once, log in everywhere</h3>
        <p>When you scan a Clef Wave, you'll be logged in to all of your sites on that computer. This means you don't have to keep scanning as you browse the web.</p>
        <h3>2. Log out with your phone</h3>
        <p>When you want to log out of your sites, click the logout button <b>on your phone</b>. This will log you out of all of your sites and can be done from anywhere (including after you walk away).</p>
        <h3>3. Lose your device?</h3>
        <p>If you lose your device, don't fret! Just visit <a href="https://getclef.com/lost">getclef.com/lost</a>, deactivate with your PIN, and reactivate on a new device.</p>
